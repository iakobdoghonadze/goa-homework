let num = Number(prompt("Enter a number (1-10):"));

if (num == 7 || num == 3) {
    alert("გაიმარჯვე!")
}
else if (num > 8 ){
    alert("ძალიან მაღალი!")
}
else if (num < 2 ){
    alert("ძალიან დაბალია!")
}

else {
    alert("კიდევ სცადე!")
}

// შეიყვანე რიცხვი 1–10.
// თუ ტოლია 7-ის ან 3-ის → "გაიმარჯვე!",
// თუ მეტი 8-ზე → "ძალიან მაღალი!",
// თუ ნაკლებია 2-ზე → "ძალიან დაბალია!",
// სხვა შემთხვევაში → "ცადე კიდევ!".